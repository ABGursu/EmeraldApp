class PrefilledMuscle {
  final String name;
  final String groupName;

  const PrefilledMuscle(this.name, this.groupName);
}

class PrefilledMuscleImpact {
  final String muscleName;
  final int rate;

  const PrefilledMuscleImpact(this.muscleName, this.rate);
}

